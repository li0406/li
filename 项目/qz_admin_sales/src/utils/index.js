export function createUniqueString() {
  const timestamp = +new Date() + ''
  const randomNum = parseInt((1 + Math.random()) * 65536) + ''
  return (+(randomNum + timestamp)).toString(32)
}

/**
 * 时间戳格式化成yyyy-mm-dd，如果是时间戳，请自行乘以1000再传入
 * @param date
 * @param sym 分隔符，默认是'-'
 * @returns {string}
 */
export function fortmatDate(date, sym) {
  if (!date) {
    return
  }
  date = new Date(date)
  if (sym) {
    return date.getFullYear() + sym + (date.getMonth() + 1) + sym + date.getDate()
  } else {
    return date.getFullYear() + '-' + (date.getMonth() + 1) + '-' + date.getDate()
  }
}

/**
 * 时间戳格式化成yyyy-mm-dd
 * @param date
 * @returns {string}
 */
export function formatDate(date) {
  if (!date) {
    return
  }
  date = new Date(date * 1000)
  const y = date.getFullYear()
  const m = date.getMonth() > 8 ? date.getMonth() + 1 : '0' + (date.getMonth() + 1)
  const d = date.getDate() > 9 ? date.getDate() : '0' + date.getDate()
  return y + '-' + m + '-' + d
}

